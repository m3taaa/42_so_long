#include "ft_printf.h"

int	ft_printf(const char *format, ...)
{
	int		i;
	int		count;
	va_list	ap;

	i = 0;
	count = 0;
	va_start(ap, format);
	while (format[i])
	{
		if (format[i] == '%')
		{
			i++;
			search_flag(format[i], &count, &ap);
		}
		else
			count += write(1, &format[i], 1);
		i++;
	}
	va_end (ap);
	return (count);
}
