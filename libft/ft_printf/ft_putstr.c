#include "ft_printf.h"

int	ft_putstr(char *s)
{
	size_t	len;

	if (s == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	len = ft_strlen(s);
	write(1, &s[0], len);
	return (len);
}
